public class Dosen {
    public String Kode;
    public String Nama;
    public Boolean JenisKelamin;
    public int usia;

    public Dosen(String Kode, String Nama , Boolean JenisKelamin, int usia) {
        this.Kode = Kode;
        this.Nama = Nama;
        this.JenisKelamin = JenisKelamin;
        this.usia = usia;
    }
}